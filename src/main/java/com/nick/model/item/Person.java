package com.nick.model.item;

public class Person {
    public String firstName;
    public String lastName;
    public int age;
    public String Phone;

    public Person(String firstName,
            String lastName,
            int age,
            String Phone) {
        this.firstName = firstName;
        this.lastName = lastName;
        this.age = age;
        this.Phone = Phone;
    }
}
